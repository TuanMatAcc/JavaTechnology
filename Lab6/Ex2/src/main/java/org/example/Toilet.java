package org.example;

public class Toilet {
    private String type;

    public Toilet() {
        this.type = type;
    }

    public Toilet(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String toString() {
        return "Toilet{" + type + "}";
    }
}